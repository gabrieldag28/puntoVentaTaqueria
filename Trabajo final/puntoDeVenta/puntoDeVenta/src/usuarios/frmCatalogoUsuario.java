    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String Nombre = txtNombre.getText();
        String apellido_paterno=txtApellidoP.getText();
        String apellido_materno=txtApellidoM.getText();
        String email=txtEmail.getText();
        String usuario=txtUcuario.getText();
        String password=txtPassword.getText();
        int id=0;
        if(modificando){
            id=Integer.parseInt(txtid.getText());
        }
        if(Nombre.length()== 0 || apellido_paterno.length()== 0||apellido_materno.length()==0||email.length()==0 ||usuario.length()==0||password.length()==0)
        {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos para registrar");
            return;
        }        
        try{
            PreparedStatement consulta;
            if(modificando){
            consulta=conexion.prepareStatement(""+"UPDATE  usuarios SET nombre=?,apellido_paterno=?,apellido_materno=?,email=?,usuario=?,contrasena=? WHERE id_usuario=?");
            consulta.setString(1, Nombre);
            consulta.setString(2, apellido_paterno);
            consulta.setString(3, apellido_materno);
            consulta.setString(4, email);
            consulta.setString(5, usuario);
            consulta.setString(6, password);
            consulta.setInt(7,id);
            }else{
            consulta=conexion.prepareStatement(""+"INSERT INTO usuarios (nombre, apellido_paterno,apellido_materno,email,usuario,contrasena)VALUES(?,?,?,?,?,?)");
            consulta.setString(1, Nombre);
            consulta.setString(2, apellido_paterno);
            consulta.setString(3, apellido_materno);
            consulta.setString(4, email);
            consulta.setString(5, usuario);
            consulta.setString(6, password);
            }
            
            consulta.executeUpdate();
            
            llenarTablaUsuarios();
            limpiar();
            btnGuardar.setText("Guardar");
            txtid.setText("   Nuevo");
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

